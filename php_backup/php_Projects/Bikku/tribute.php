  <p class="t"><b>About</b> : Snoop Dogg is a West Coast rapper who evolved under the tutelage of Dr. Dre, and has received fame for albums such as 'Doggystyle,' 'Tha Doggfather' and 'Reincarnated.'He was born Cordozar Calvin Broadus Jr. in Long Beach, California, on October 20, 1971. His "Snoop Dogg" nickname came from his mother because she thought he looked like Snoopy from the Peanuts cartoon. He played piano and sang at his local Baptist church, before starting rap in sixth grade. After graduating high school, Snoop was arrested several times for drug possession and spent time in prison. He was also associated with the Rollin’ 20 Crips Gang. He started making music as a way out of his troubles and recorded early demos with his cousin Nate Dogg and friend Warren G as 213. A track on one of these came to the attention of Dr. Dre who invited Snoop — then rapping under the name Snoop Doggy Dogg — to audition. From there they collaborated on a song called "Deep Cover" for the soundtrack of the film for the same name; and Snoop became the key rapper on Dre’s hugely successful first solo album, The Chronic, in 1992.

