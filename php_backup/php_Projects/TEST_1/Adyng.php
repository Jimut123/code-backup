<section class="Main_Body">
Adam Young is a singer, songwriter and multi-instrumentalist born in 5th July,  1986.  Known to be the founder of "Owl City", an American 
<a href="https://en.wikipedia.org/wiki/Electronica" alt="electronica" target="_blank">
electronica
</a>
project created in 2007 in 
<a href="https://en.wikipedia.org/wiki/Owatonna,_Minnesota" alt="Minnesota" target="_blank">
Owatonna,Minnesota.
</a>

Young created the project while experimenting with music in his parent's basement.Adam gained mainstream popularity with the release of

<a href="https://en.wikipedia.org/wiki/Fireflies_(Owl_City_song)" alt="Fireflies" target="_blank">
"Fireflies"
</a>
which includes the six-time platinum singles. He graduated from Owatonna Senior High School and then found work at a Coca-Cola warehouse. He began composing melodies in his head while working, which he would then record in his studio in his parent's basement, while he suffered from insonomia, a disease in which you couldn't sleep for long. Young has described himself as an intovert and he is a devout Christian.
